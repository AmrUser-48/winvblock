struct WV_DISK_FAT_EXTRA {
    UCHAR bs_drvnum;
    UCHAR bs_resv1;
    UCHAR bs_bootsig;
    UINT32 bs_volid;
    char bs_vollab[11];
    char bs_filsystype[8];
  } __attribute__((packed));
typedef struct WV_DISK_FAT_EXTRA WV_S_DISK_FAT_EXTRA, * WV_SP_DISK_FAT_EXTRA;

struct WV_DISK_FAT_SUPER {
    UCHAR bs_jmpboot[3];
    char bs_oemname[8];
    UINT16 bpb_bytspersec;
    UCHAR bpb_secperclus;
    UINT16 bpb_rsvdseccnt;
    UCHAR bpb_numfats;
    UINT16 bpb_rootentcnt;
    UINT16 bpb_totsec16;
    UCHAR bpb_media;
    UINT16 bpb_fatsz16;
    UINT16 bpb_secpertrk;
    UINT16 bpb_numheads;
    UINT32 bpb_hiddsec;
    UINT32 bpb_totsec32;
    union {
        struct { WV_S_DISK_FAT_EXTRA extra; } fat16;
        struct {
            UINT32 bpb_fatsz32;
            UINT16 bpb_extflags;
            UINT16 bpb_fsver;
            UINT32 bpb_rootclus;
            UINT16 bpb_fsinfo;
            UINT16 bpb_bkbootsec;
            char bpb_reserved[12];
            /* Clever, eh?  Same fields, different offset... */
            WV_S_DISK_FAT_EXTRA extra;
          } fat32 __attribute__((packed));
      } x;
  } __attribute__((__packed__));
typedef struct WV_DISK_FAT_SUPER WV_S_DISK_FAT_SUPER, * WV_SP_DISK_FAT_SUPER;
